/**
 * defines CSS RGB
 *
 * @export
 */
export class CssRedGreenBlue {
    /**
     * Creates an instance of CssRedGreenBlue.
     */
    constructor(r: number, g: number, b: number) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    /**
     * red
     */
    r: number;

    /**
     * green
     */
    g: number;

    /**
     * blue
     */
    b: number;
}
